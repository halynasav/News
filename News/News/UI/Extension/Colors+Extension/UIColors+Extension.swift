import UIKit

extension UIColor {
    class var appPrimaryBackground: UIColor { UIColor(named: "PrimaryBackgroundColor") ?? .systemBlue}
    
    class var appPrimary: UIColor { UIColor(named: "PrimaryColor") ?? .systemBlue}

    class var appSecondary: UIColor { UIColor(named: "SecondaryColor") ?? .systemBlue}
    
    
    class var appSecondaryBackground: UIColor { UIColor(named: "SecondaryBackgroundColor") ?? .systemBlue}
    
    class var appHeaderTextColor: UIColor { UIColor(named: "SectionColor") ?? .systemBlue}


    
}
